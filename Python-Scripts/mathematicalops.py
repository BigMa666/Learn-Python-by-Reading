num1  = 31
num2 = 20
# Add
sum = num1 + num2
sum2 = 31 + 20

print(sum, sum2)

# Subtraction
ans = num1 - num2
ans2 = 31 - 20

print(ans, ans2)

# Mutiplication
product = num1 * num2
product2 = 31 * 20

print(product, product2)

# Division
quotient = num1 / num2
quotient2 = 31/20

print(quotient, quotient2)